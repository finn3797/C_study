#include <iostream>
using namespace std;
int main() {
  int a;
  a = 5;
  cout << "a = 5 -> " << a << endl;
  a += 1;
  cout << "a += 1 -> " << a << endl;
  a -= 2;
  cout << "a -= 2 -> " << a << endl;
  a *= 6;
  cout << "a *= 6 -> " << a << endl;
  a /= 4;
  cout << "a /= 4 -> " << a << endl;
  a %= 4;
  cout << "a %= 4 -> " << a << endl;
}